CREATE DATABASE IF NOT EXISTS wirehome;

USE wirehome;

CREATE TABLE IF NOT EXISTS History.ComponentStatus (
    ID INT UNSIGNED NOT NULL AUTO_INCREMENT,
    
    ComponentUid VARCHAR(255) NOT NULL,
    StatusUid VARCHAR(255) NOT NULL,
    
    Value VARCHAR(512),
    IsLatest BIT NOT NULL,

	BeginTimestamp DATETIME NOT NULL,
    EndTimestamp DATETIME NOT NULL,

	PRIMARY KEY (ID)
)